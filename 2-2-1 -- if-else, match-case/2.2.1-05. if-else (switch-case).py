# if-else anidado
dia = input('Ingrese el dia de la semana: ')

if (dia == 'lunes'):
    print('Hoy es lunes')
elif (dia == 'martes'):
    print('Hoy es martes')
elif (dia == 'miercoles'):
    print('Hoy es miercoles')
elif (dia == 'jueves'):
    print('Hoy es jueves')
elif (dia == 'viernes'):
    print('Hoy es viernes')
elif (dia == 'sabado'):
    print('Hoy es sabado')
elif (dia == 'domingo'):
    print('Hoy es domingo')
else:
    print('Dia no reconocido o un valor erroneo')